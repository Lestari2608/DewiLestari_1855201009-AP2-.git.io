    private static String readTxt() {
        String filepath = "E://input23.txt";
        BufferedReader br = null;
        String content = null;
        try {
            br = new BufferedReader(new FileReader(filepath));
            StringBuilder sb = new StringBuilder();
            String line = br.readLine();

            while (line != null) {
                sb.append(line);
                line = br.readLine();
                sb.append(System.lineSeparator());
            }
            content = sb.toString();
        } catch (FileNotFoundException e) {
        } catch (IOException e) {
        } finally {
            try {
                br.close();
            } catch (IOException e) {
            }
        }
        return content;
    }

    public static void writeDoc(String teks) throws IOException {
        Properties prop = new Properties();
        prop.setProperty("log4j.rootLogger", "WARN");

        String outDocEn = "E://convertDoc.doc";
        XWPFDocument document = new XWPFDocument();
        FileOutputStream out = new FileOutputStream(new File(outDocEn));
        XWPFParagraph paragraph = document.createParagraph();
        XWPFRun run = paragraph.createRun();
        run.setText(teks);
        document.write(out);
        out.close();
        System.out.println("Generate DOC sukses");
    }

    public static void writeDocx(String text) throws FileNotFoundException, IOException {
        BasicConfigurator.configure();
//       
        XWPFDocument documentDocx = new XWPFDocument();

        // write document
        String outDocxEn = "E://convertDocx.docx";
        //create paragraph
        try (FileOutputStream outDocx = new FileOutputStream(new File(outDocxEn))) {
            //create paragraph
            XWPFParagraph paragraphDocx = documentDocx.createParagraph();
            XWPFRun runDocx = paragraphDocx.createRun();
            runDocx.setText(text);
            documentDocx.write(outDocx);

        }
        System.out.println("Write docx successfully");
    }


